package math;
public class Maths {

	public static void main(String[] args) {
		
	
		
     System.out.println(Math.abs(-160));	
     System.out.println(Math.sin(90));
     System.out.println(Math.cos(90));
     System.out.println(Math.tan(90));
     System.out.println(Math.log(100));
     System.out.println(Math.log10(100));
     System.out.println(Math.max(2,3));
     System.out.println(Math.max(2.3,3.2));
     System.out.println(Math.min(2,3));
     System.out.println(Math.min(2.3,3.2));
     System.out.println(Math.floor(7.4));
     System.out.println(Math.round(7.4));
     System.out.println(Math.random());
     System.out.println(Math.pow(2,8));
     System.out.println(Math.pow(2.0,8.0));
    
	}

	

}
